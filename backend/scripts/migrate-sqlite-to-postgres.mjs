function quoteIdentifier(value) {
  return `"${String(value).replace(/"/g, "\"\"")}"`;
}

function mapColumnType(column) {
  const type = String(column.type ?? "").toUpperCase();

  if (type.includes("INT")) {
    return "BIGINT";
  }

  if (type.includes("REAL") || type.includes("FLOA") || type.includes("DOUB")) {
    return "DOUBLE PRECISION";
  }

  if (type.includes("BLOB")) {
    return "BYTEA";
  }

  return "TEXT";
}

function formatDefaultValue(defaultValue) {
  if (defaultValue == null) {
    return "";
  }

  const raw = String(defaultValue).trim();

  if (!raw) {
    return "";
  }

  if (raw.toUpperCase() === "CURRENT_TIMESTAMP") {
    return " DEFAULT CURRENT_TIMESTAMP";
  }

  if (/^-?\d+(\.\d+)?$/.test(raw)) {
    return ` DEFAULT ${raw}`;
  }

  const normalized = raw.replace(/^'(.*)'$/, "$1");
  return ` DEFAULT '${normalized.replace(/'/g, "''")}'`;
}

function buildCreateTableSql(tableName) {
  const columns = sqlite.prepare(`PRAGMA table_info(${quoteIdentifier(tableName)})`).all();
  const foreignKeys = sqlite.prepare(`PRAGMA foreign_key_list(${quoteIdentifier(tableName)})`).all();
  const primaryKeys = columns.filter((column) => Number(column.pk) > 0).sort((a, b) => a.pk - b.pk);

  const definitions = columns.map((column) => {
    const isSinglePrimary = primaryKeys.length === 1 && primaryKeys[0].name === column.name;
    const isIdentity = isSinglePrimary && String(column.type ?? "").toUpperCase().includes("INT") && column.name === "id";
    const baseType = mapColumnType(column);

    let line = `${quoteIdentifier(column.name)} ${baseType}`;

    if (isIdentity) {
      line += " GENERATED BY DEFAULT AS IDENTITY";
    }

    if (Number(column.notnull) === 1) {
      line += " NOT NULL";
    }

    if (isSinglePrimary) {
      line += " PRIMARY KEY";
    } else {
      line += formatDefaultValue(column.dflt_value);
    }

    return line;
  });

  if (primaryKeys.length > 1) {
    definitions.push(`PRIMARY KEY (${primaryKeys.map((column) => quoteIdentifier(column.name)).join(", ")})`);
  }

  for (const foreignKey of foreignKeys) {
    definitions.push(
      `FOREIGN KEY (${quoteIdentifier(foreignKey.from)}) REFERENCES ${quoteIdentifier(foreignKey.table)} (${quoteIdentifier(foreignKey.to)})`,
    );
  }

  return `CREATE TABLE IF NOT EXISTS ${quoteIdentifier(tableName)} (\n  ${definitions.join(",\n  ")}\n);`;
}

function getTableNames() {
  const existing = sqlite.prepare(`
    SELECT name
    FROM sqlite_master
    WHERE type = 'table'
      AND name NOT LIKE 'sqlite_%'
    ORDER BY name
  `).all().map((row) => row.name);

  return tableOrder.filter((table) => existing.includes(table));
}

function getUniqueIndexStatements(tableName) {
  const indexes = sqlite.prepare(`PRAGMA index_list(${quoteIdentifier(tableName)})`).all();

  return indexes
    .filter((index) => Number(index.unique) === 1 && index.origin !== "pk")
    .map((index) => {
      const columns = sqlite.prepare(`PRAGMA index_info(${quoteIdentifier(index.name)})`).all();
      const columnNames = columns.map((column) => quoteIdentifier(column.name)).join(", ");
      return `CREATE UNIQUE INDEX IF NOT EXISTS ${quoteIdentifier(index.name)} ON ${quoteIdentifier(tableName)} (${columnNames});`;
    });
}

function normalizeValue(value) {
  if (value == null) {
    return null;
  }

  if (Buffer.isBuffer(value)) {
    return value;
  }

  return value;
}

async function run() {
  await pgClient.connect();
  await pgClient.query("BEGIN");

  try {
    const tables = getTableNames();

    for (const tableName of tables) {
      await pgClient.query(buildCreateTableSql(tableName));
      for (const statement of getUniqueIndexStatements(tableName)) {
        await pgClient.query(statement);
      }
    }

    for (const tableName of [...tables].reverse()) {
      await pgClient.query(`TRUNCATE TABLE ${quoteIdentifier(tableName)} RESTART IDENTITY CASCADE`);
    }

    for (const tableName of tables) {
      const rows = sqlite.prepare(`SELECT * FROM ${quoteIdentifier(tableName)}`).all();

      if (rows.length === 0) {
        continue;
      }

      const columns = Object.keys(rows[0]);
      const quotedColumns = columns.map(quoteIdentifier).join(", ");

      for (const row of rows) {
        const values = columns.map((column) => normalizeValue(row[column]));
        const placeholders = values.map((_, index) => `$${index + 1}`).join(", ");
        await pgClient.query(
          `INSERT INTO ${quoteIdentifier(tableName)} (${quotedColumns}) VALUES (${placeholders})`,
          values,
        );
      }
    }

    for (const tableName of tables) {
      const hasId = sqlite.prepare(`PRAGMA table_info(${quoteIdentifier(tableName)})`).all().some((column) => column.name === "id");

      if (hasId) {
        await pgClient.query(`
          SELECT setval(
            pg_get_serial_sequence($1, 'id'),
            COALESCE((SELECT MAX(id) FROM ${quoteIdentifier(tableName)}), 1),
            true
          )
        `, [tableName]);
      }
    }

    await pgClient.query("COMMIT");
    console.log(`Migrated ${tables.length} tables from SQLite to Neon Postgres.`);
  } catch (error) {
    await pgClient.query("ROLLBACK");
    throw error;
  } finally {
    sqlite.close();
    await pgClient.end();
  }
}

run().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
